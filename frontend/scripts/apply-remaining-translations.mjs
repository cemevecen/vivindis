function deepMerge(target, patch) {
  const out = { ...target };
  for (const k of Object.keys(patch)) {
    const pv = patch[k];
    const tv = target[k];
    if (pv !== null && typeof pv === "object" && !Array.isArray(pv) && tv !== null && typeof tv === "object" && !Array.isArray(tv)) {
      out[k] = deepMerge(tv, pv);
    } else {
      out[k] = pv;
    }
  }
  return out;
}

